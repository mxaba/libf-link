#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char	*src;
	int		i;
	int		j;

	i = 0;
	src = (char *)s2;
	while (s1[i])
		i++;
	j = 0;
	while (src[j])
	{
		s1[i] = src[j];
		i++;
		j++;
	}
	s1[i] = '\0';
	return (s1);
}
